// Global test utilities
global.testUtils = {
  waitForElementToBeRemoved: (element: Element) =>
    new Promise((resolve) => {
      const observer = new MutationObserver(() => {
        if (!document.contains(element)) {
          observer.disconnect();
          resolve(true);
        }
      });
      observer.observe(document.body, { childList: true, subtree: true });
    }),
};
